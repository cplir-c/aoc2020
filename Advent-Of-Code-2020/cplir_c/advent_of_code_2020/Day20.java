    static void addTileEdge(Short2ObjectMap<Byte2ObjectMap<ObjectSet<Tile>>> tilesByEdge, Tile tile, short edge,
                            byte orientation) {
        var orientationName = Edge.EdgeOrientation.EDGE_ORIENTATIONS[orientation];
        if (tilesByEdge.computeIfAbsent(edge, NEW_BYTE2TILESET_MAP).computeIfAbsent(orientation, NEW_TILESET).add(tile)) {
            // System.out.println("added " + orientationName + " edge " + edge + " of tile " + tile);
        } else {
            System.out.println("already contained " + orientationName + " edge " + edge + " of tile " + tile);
        }
    }

    static int sqrt(int area) {
        var upper = (area >> 1) + (area & 1);
        var lower = 1;
        while (upper - lower > 1) {
            var mid = (upper + lower) >> 1;
            System.out.println(mid);
            if (mid * mid <= area) {
                lower = mid;
            } else {
                upper = mid;
            }
        }
        return lower;
    }

    static Short2ObjectMap<Tile> parseTiles(String input) {
        input = input.replace('.', '0').replace('#', '1');
        var                   tileStrings = LINES.split(input);
        Short2ObjectMap<Tile> tileMap     = new Short2ObjectOpenHashMap<>(tileStrings.length);
        for (String tileString : tileStrings) {
            var tileLines    = TILE_LINE.split(tileString);
            var tileIDString = tileLines[0];
            var tileID       = Short.parseShort(tileIDString.substring("Tile ".length(), tileIDString.length()));
            var tile         = Tile.fromStringArray(tileLines, 1, tileLines.length, tileID);
            tileMap.put(tileID, tile);
        }
        return tileMap;
    }

